#include <stdio.h>

int main()
{
    int a = 24, b = 54; // Here I have assign values to a and b
    printf("\nArithmetic Operators:\n"); // Display the operator name

    printf("a + b = %d\n", a + b);      // It will add a & b

    printf("a - b = %d\n", a - b);      // It will subtract a & b

    printf("a * b = %d\n", a * b);      // It will multiply a & b

    printf("a / b = %d\n", a / b);      // It will divide a & b and give the quotient

    printf("a %% b = %d\n", a % b);     // It will give the remainder of a & b

    return 0;
}
